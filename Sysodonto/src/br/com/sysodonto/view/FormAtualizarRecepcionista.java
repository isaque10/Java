    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Recepcionista r = new Recepcionista();
        r.setId(Integer.parseInt(txtId.getText()));
        r.setNome(txtNome.getText());
        r.setCpf(txtCpf.getText());
        r.setEmail(txtEmail.getText());
        r.setTelefone(txtTelefone.getText());
        r.setEndereco(txtEndereco.getText());
        BRecepcionista bp = new BRecepcionista();
        
        if(bp.atualizarRecepcionista(r)){
            JOptionPane.showMessageDialog(null, "Recepcionista Atualizada!");
            limparCampos();
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao atualizar Recepcionista!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        this.setVisible(false);
        limparCampos();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        BRecepcionista bp = new BRecepcionista();
        int excluir = JOptionPane.showConfirmDialog(null, "Deseja excluir a Recepcionista?");
        
        if(excluir == 0){
            if(bp.excluirRecepcionista(Integer.parseInt(txtId.getText()))){
                JOptionPane.showMessageDialog(null, "Recepcionista excluída com sucesso!");  
                this.setVisible(false);
                limparCampos();
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao excluir recepcionista!");               
            }
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed
    
    private void limparCampos(){
        txtCpf.setText("");
        txtId.setText("");
        txtNome.setText("");
        txtTelefone.setText("");
        txtEmail.setText("");
        txtEndereco.setText("");
    }

