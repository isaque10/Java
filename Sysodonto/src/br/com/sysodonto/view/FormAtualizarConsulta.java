    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        BConsulta bc = new BConsulta();
        
        int excluir = JOptionPane.showConfirmDialog(null, "Deseja excluir a Consulta?");
        
        if(excluir == 0){
            if(bc.excluirConsulta(Integer.parseInt(txtId.getText()))){
                JOptionPane.showMessageDialog(null, "Consulta exclu√≠da com sucesso!");  
                this.setVisible(false);
                
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao excluir Consulta!");               
            }
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed
    
    
