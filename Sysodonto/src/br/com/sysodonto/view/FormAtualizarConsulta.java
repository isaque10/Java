    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        Consulta consulta = new Consulta();
        consulta.setId(Integer.parseInt(txtId.getText()));
        consulta.setData(txtData.getCalendar());
        consulta.setHora(String.valueOf(cbHora.getSelectedItem()));
        
        BConsulta bc = new BConsulta();
        
        if(bc.atualizarConsulta(consulta)){
            JOptionPane.showMessageDialog(null, "Consulta Atualizada!");
            this.setVisible(false);
        
            
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao atualizar Consulta!");
        
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

